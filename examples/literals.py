# List
names = ['Euclid', 'Lovelace', 'Turing']

# Tuples (immutible)
names = ('Euclid', 'Lovelace', 'Turing')

# To specify a one-tuple
names = ('Euclid',)

# Dictionaries
names = {'Lovelace': 'Ada', 'Turing': 'Alan'}

# Sets (unique values)
names = {'Euclid', 'Lovelace', 'Turing'}

# The empty set (to distinguish from a dictionary)
names = set()
